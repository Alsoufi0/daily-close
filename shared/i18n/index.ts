export function normalizeLanguage(value?: string | null): Language {
  const code = (value || "en").toLowerCase().slice(0, 2);
  return code === "ar" || code === "es" || code === "hi" ? code : "en";
}

export function languageDir(lang: Language): "ltr" | "rtl" {
  return lang === "ar" ? "rtl" : "ltr";
}

export function translate(lang: Language, key: string): string {
  return (
    productionTranslations[lang]?.[key] ??
    supplementalTranslations[lang]?.[key] ??
    translations[lang]?.[key] ??
    productionTranslations.en[key] ??
    supplementalTranslations.en[key] ??
    translations.en[key] ??
    key
  );
}
